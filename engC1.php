                                    <h2 class="featurette-heading fw-normal lh-1">ภาษาอังกฤษ ป. 6 : Adverb ตะลุยโจทย์<span class="text-body-secondary"></span></h2><br>

                                    <p>Adverb(คำกริยาวิเศษณ์) ทำหน้าที่ในการขยาย verb, adjective ,adverb <br><br>
                                        Adverb แบ่งเป็นประเภทต่าง ๆ ดังนี้<br>
                                        1. Adverb of Manner คือ คำที่ใช้อธิบายลักษณะท่าทางอาการ <br>
                                        2. Adverb of Place คือ คำที่ใช้บอกสถานที่ หรือคำที่ให้ความหมายเกี่ยวกับสถานที่
                                        <br>
                                        3. Adverb of Time คือ คำที่ใช้บอกเวลา <br>
                                        4. Adverb of Frequency คือ คำที่ใช้บอกถึงความถี่ <br>
                                        5. Adverb of Degree คือ คำที่ใช้บอกว่าอยู่ในระดับไหน หรือใช้เน้นย้ำ <br><br>

                                        หน้าที่หลักของ Adverb<br>
                                        ขยายกริยาในประโยค,ขยายคำคุณศัพท์ในประโยค,ขยายคำกริยาวิเศษณ์ด้วยกันเอง<br><br>

                                        ลำดับการเรียง Adverb มีดังนี้<br>
                                        Manner > Place > Frequency > Time > Purpose
                                        <br>
