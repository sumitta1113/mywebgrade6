                                <p>Adjective (Adj.)ทำหน้าที่ขยายคำนามหรือสรรพนามที่อยู่ในประโยค<br>
                                    เพื่อบอกให้รู้ว่าคำนามหรือสรรพนามเหล่านั้นมีลักษณะอย่างไร เช่น สูง ต่ำ ดำ ขาว  ฯลฯ <br><br>
                                    โดยคำคุณศัพท์ จะวางอยู่ในส่วนต่าง ๆ ในประโยคดังต่อไปนี้<br>

                                    - วางขยายหน้าคำนามนั้น ๆ เช่น a red rose (ดอกกุหลาบสีแดง)<br>
                                    - วางไว้หลัง Verb to be (be, is, am, are, was, were, been) <br>
                                    เช่น The rose is beautiful. (ดอกกุหลาบดอกนั้นสวยจัง)<br>
                                    - วางไว้หลังกริยาเชื่อม (Linking Verb : look, sound, taste, feel) <br>
                                    เช่น You look beautiful in this dress. (คุณดูสวยในชุดเดรสชุดนี้นะ)
